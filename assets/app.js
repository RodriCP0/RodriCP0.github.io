/* ---------- CURSOR ---------- */
(function(){
  if (!('matchMedia' in window) || window.matchMedia('(hover:none)').matches) return;
  const dot = document.getElementById('cursorDot');
  const ring = document.getElementById('cursorRing');
  let mx=0, my=0, rx=0, ry=0;
  window.addEventListener('mousemove', e => {
    mx = e.clientX; my = e.clientY;
    dot.style.left = mx+'px'; dot.style.top = my+'px';
  });
  function animate(){
    rx += (mx-rx)*0.18; ry += (my-ry)*0.18;
    ring.style.left = rx+'px'; ring.style.top = ry+'px';
    requestAnimationFrame(animate);
  }
  animate();
  const attach = ()=>{
    document.querySelectorAll('a, button, .bento-card, .mosaic-item, .service-feature, .contact-profile, .value-card').forEach(el=>{
      if (el.dataset.ch) return;
      el.dataset.ch = '1';
      el.addEventListener('mouseenter', ()=>document.body.classList.add('hovering'));
      el.addEventListener('mouseleave', ()=>document.body.classList.remove('hovering'));
    });
  };
  attach();
  new MutationObserver(attach).observe(document.body, {childList:true, subtree:true});
})();

/* ---------- MOBILE DRAWER ---------- */
function toggleDrawer(){
  const drawer = document.getElementById('drawer');
  if (!drawer) return;
  drawer.classList.toggle('open');
  document.body.style.overflow = drawer.classList.contains('open') ? 'hidden' : '';
}
window.toggleDrawer = toggleDrawer;

/* ---------- FORM ---------- */
function submitForm(e){
  e.preventDefault();
  const form = e.target;
  const btn = form.querySelector('button[type="submit"] span:first-child');
  const orig = btn.textContent;
  const sent = LANG==='es' ? '✓ Enviado, gracias' : LANG==='pt' ? '✓ Enviado, obrigado' : '✓ Sent, thank you';
  btn.textContent = sent;
  btn.closest('.btn').style.background = 'var(--coral)';
  setTimeout(()=>{ form.reset(); btn.textContent = orig; btn.closest('.btn').style.background=''; }, 3200);
}

/* ---------- COUNT UP ---------- */
function countUp(){
  document.querySelectorAll('[data-count]').forEach(el=>{
    const target = parseInt(el.dataset.count,10);
    const suffix = el.dataset.suffix || '';
    let cur = 0;
    const step = Math.max(1, Math.ceil(target/30));
    const iv = setInterval(()=>{
      cur += step;
      if (cur >= target){ cur = target; clearInterval(iv); }
      el.textContent = cur + suffix;
    }, 35);
  });
}

