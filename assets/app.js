let LANG = _getStore('smn_lang') || 'es';

/* ---------- APPLY I18N ---------- */
function applyI18n(){
  const t = I18N[LANG];
  document.documentElement.lang = LANG;
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.getAttribute('data-i18n');
    if (t[key]===undefined) return;
    if (el.tagName === 'OPTION'){ el.textContent = t[key]; }
    else { el.innerHTML = t[key]; }
  });
  document.querySelectorAll('[data-ph-key]').forEach(el=>{
    const key = el.getAttribute('data-ph-key');
    if (t[key]) el.placeholder = t[key];
  });
  // Ticker
  const track = document.getElementById('ticker');
  if (track){
    const items = [...t.ticker, ...t.ticker, ...t.ticker];
    track.innerHTML = items.map(i=>`<span class="ticker-item">${i}</span>`).join('');
  }
  // Lang buttons
  document.querySelectorAll('.lang-btn').forEach(b=>{
    b.classList.toggle('active', b.textContent.trim().toLowerCase() === LANG);
  });
}

function setLang(l){
  LANG = l;
  _setStore('smn_'+'lang', l);
  applyI18n();
}

